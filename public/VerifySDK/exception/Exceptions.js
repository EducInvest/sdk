export const SDKExceptionCode = {
  // Inicialização
  NOT_INITIALIZED: 'NOT_INITIALIZED',
  INVALID_API_KEY: 'INVALID_API_KEY',
  INITIALIZATION_FAILED: 'INITIALIZATION_FAILED',

  // Verificação
  INVALID_WORKFLOW_ID: 'INVALID_WORKFLOW_ID',
  VERIFICATION_EXPIRED: 'VERIFICATION_EXPIRED',
  INVALID_THIRD_PARTY_REF: 'INVALID_THIRD_PARTY_REF',
  VERIFICATION_CREATION_FAILED: 'VERIFICATION_CREATION_FAILED',
  VERIFICATION_FETCH_FAILED: 'VERIFICATION_FETCH_FAILED',
  VERIFICATION_REVOKE_FAILED: 'VERIFICATION_REVOKE_FAILED',

  // Serviço / Processors
  PROCESSOR_START_FAILED: 'PROCESSOR_START_FAILED',
  FACETEC_SESSION_FAILED: 'FACETEC_SESSION_FAILED',

  // Comunicação com servidor
  NETWORK_ERROR: 'NETWORK_ERROR',
  SERVER_ERROR: 'SERVER_ERROR',
  RESPONSE_PARSING_ERROR: 'RESPONSE_PARSING_ERROR',

  // Dados inválidos
  MISSING_REQUIRED_FIELDS: 'MISSING_REQUIRED_FIELDS',
  INVALID_REQUEST_FORMAT: 'INVALID_REQUEST_FORMAT',

  // Desconhecido
  UNKNOWN: 'UNKNOWN',
};

/**
 * Exceção personalizada do SDK.
 */
export class SDKException extends Error {
  constructor(message, code = SDKExceptionCode.UNKNOWN) {
    super(message);
    this.name = 'SDKException';
    this.code = code;
  }
}